package botLoader;

/**
* Here we store values of what our Interactive app's button IDs are
* so we can have them correspond to the appropriate key
* Also, since calling upon an enum as an array of values produces them
* in the same order they're defined, I can simply list them buttons in order
* with their ID on beam
*/
public enum KeyValues {
    RBRACKET,
    BACKSPACE,
    LBRACKET,
    MINUS,
    PLUS,
    PERIOD,
    COMMA,
    LTHAN,
    GTHAN,
    UP,
    DOWN,
    LEFT,
    RIGHT,
    BUILD,
    STOP
}
